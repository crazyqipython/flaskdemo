/*1\23.4四个数，能组成几个三个位，且三位数每一位的数都不相同*/
#include<stdio.h>
void main(){
	int i,j,k;
	for (i=1;i<5;i++){
		for (j=1;j<5;j++){
			for (k=1;k<5;k++){
			    if(i!=k&&j!=i&&k!=j){
					printf("%d%d%d\n",i,j,k);
				}
			}
		}
	}
}
