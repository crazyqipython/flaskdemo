/*猴子吃桃，每天吃前面的一半加一个，第十天只剩下１个了，第一个摘了多少*/
#include<stdio.h>
void main(){
	int day,s1,s2;
	day=9;
	s1=1;
    while(day>0){
		s2=2*s1+1;
		s1=s2;
		--day;
	}
	printf("the total is %d\n",s1);
}

